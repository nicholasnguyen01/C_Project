#include <iostream>

using namespace std;

int main() {

    int num1,num2;
    cout << "Enter your first integer number: " << endl;
    cin >> num1;

    cout << "Enter your second integer number: " << endl;
    cin >> num2;

    cout << "Your integer number is: " << num1 + num2 << endl;

    double num3,num4;
    cout << "Enter your first double number: " << endl;
    cin >> num3;

    cout << "Enter your second double number: " << endl;
    cin >> num4;

    cout << "Your double number is: " << num3 + num4 << endl;

    return 0;
}
